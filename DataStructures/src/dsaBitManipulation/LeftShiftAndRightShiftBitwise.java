package dsaBitManipulation;

public class LeftShiftAndRightShiftBitwise {
	
	
	
public static void main(String[] args) {
		
		
		int a =5;
		
		
		// bitwise right shift operator
		
		System.out.println(a>>1);//5/2\power1 ==> 5/2 =2
		
		System.out.println(a>>4);//5/2power4 ===>5/16 ==0
		
		// bitwise left shift operator.
		
		System.out.println(a<<2);//5*2power2 ===>5*4 =20
		
		System.out.println(a<<4);//5*2power6 ===>5*16=80
		
		
}

}
